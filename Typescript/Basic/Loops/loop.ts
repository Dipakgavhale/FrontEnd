/*
looping statements
for, while, do_while, foreach
*/
console.log("Using for loop.");
for(var i=0;i<=5;i++){
    console.log("i = "+i);
}
console.log("Using while loop.");
let count = 5;
while(count != 0){
    console.log("count = "+count);
    count--;
}
console.log("Using do_while loop.");
let count1 = 5;
do{
    console.log("count1 = "+count1);
    count1--;
}while(count1 != 0)