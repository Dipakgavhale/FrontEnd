var bool = true;
// Angle bracket
var temp1 = bool;
console.log("Value of A is convert boolean into String : " + temp1); // "true"
// as syntax
var temp2 = bool;
console.log("Value of A is convert boolean into number : " + temp2); // true
var str10 = "Dipak";
